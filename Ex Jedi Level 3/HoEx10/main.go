package main

import "fmt"

func main() {
	fmt.Printf("Answer for true && true is: %v\n", true && true)
	fmt.Printf("Answer for true && false is: %v\n", true && false)
	fmt.Printf("Answer for true || true is: %v\n", true || true)
	fmt.Printf("Answer for true || false is: %v\n", true || false)
	fmt.Printf("Answer for not true is: %v\n", !true)

}
